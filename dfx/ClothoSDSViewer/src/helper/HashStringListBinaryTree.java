package helper;

import java.util.HashMap;
import newsds.datastructures.SDSBinaryTree;

/**
 *
 * @author vvasilev
 */
public class HashStringListBinaryTree extends HashMap<StringList, SDSBinaryTree> {

    public HashStringListBinaryTree() {
        super();
    }

    public HashStringListBinaryTree(HashStringListBinaryTree hash1, HashStringListBinaryTree hash2) {
        super();
        this.putAll(hash1);
        this.putAll(hash2);
    }

}
