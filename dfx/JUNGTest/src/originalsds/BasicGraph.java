package originalsds;

/**
 *
 * @author vvasilev
 */
public class BasicGraph {

    protected Node _node;

    public BasicGraph() {
        _node = new Node();
        resetCost();
    }

    public CompositePart getPart()
    {
        return _node.getCompositePart();
    }

    public void resetNodeColor() {
        _node.resetColor();
    }

    public final void resetCost() {
        _node.resetCost();
    }

    public int getStages() {
        return _node.getStages();
    }

    public int getSteps() {
        return _node.getSteps();
    }

    public int getSharing() {
        return _node.getSharing();
    }

    public void setStages(int stages) {
        _node.setStages(stages);
    }

    public void setSteps(int steps) {
        _node.setSteps(steps);
    }

    public void setSharing(int sharing) {
        _node.setSharing(sharing);
    }
}
